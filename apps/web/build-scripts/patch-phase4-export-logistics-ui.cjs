'use strict';
const fs=require('fs');
const path=require('path');
const root=path.resolve(__dirname,'..');
const htmlPath=path.join(root,'apps/web/index.html');
const jsPath=path.join(root,'apps/web/phase4-export-logistics-ui.js');
const cssPath=path.join(root,'apps/web/phase4-export-logistics-ui.css');
const start='<!-- SG_PHASE43_EXPORT_LOGISTICS_UI_START -->';
const end='<!-- SG_PHASE43_EXPORT_LOGISTICS_UI_END -->';
let html=fs.readFileSync(htmlPath,'utf8');
let js=fs.readFileSync(jsPath,'utf8');
const css=fs.readFileSync(cssPath,'utf8');
const documentOld="<br>Fletë-Dalja: '+esc(x.deliveryDocumentNo||'—')+'</p></div>";
const documentNew="<br>Fletë-Dalja: '+esc(x.deliveryDocumentNo||'—')+'<br>POD: '+esc(x.deliveryProofRef||'—')+'</p></div>";
if(js.includes(documentOld))js=js.replace(documentOld,documentNew);
else if(!js.includes(documentNew))throw new Error('Mungon seksioni i dokumenteve të ngarkesës për POD.');
const escStart=start.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
const escEnd=end.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
html=html.replace(new RegExp(escStart+'[\\s\\S]*?'+escEnd+'\\s*','g'),'');
const finalClose=/<\/body>\s*<\/html>\s*$/i;
if(!finalClose.test(html))throw new Error('Mungon mbyllja finale </body></html>.');
const block=start+'\n<style id="sg-phase43-export-logistics-style">\n'+css+'\n</style>\n<script id="sg-phase43-export-logistics-script">\n'+js+'\n</script>\n'+end+'\n';
html=html.replace(finalClose,block+'</body>\n</html>');
fs.writeFileSync(htmlPath,html);
const check=fs.readFileSync(htmlPath,'utf8');
if((check.match(/SG_PHASE43_EXPORT_LOGISTICS_UI_START/g)||[]).length!==1)throw new Error('Patch-i Phase 4.3 nuk është idempotent.');
['Ngarkesat / Eksporti','Kartela e Automjeteve','editExportShipment','editLogisticsVehicle','printPackingList','printCMR','exportShipmentPDF','exportCurrentExportReportExcel','global.SGPhase43=App.SGPhase43','POD:'].forEach(function(marker){if(!check.includes(marker))throw new Error('Mungon '+marker+' në HTML.');});
console.log('Phase 4.3 export/logistics UI patched.');
