'use strict';
const fs=require('fs');
const path=require('path');
const root=path.resolve(__dirname,'..');
const htmlPath=path.join(root,'apps/web/index.html');
const jsPath=path.join(root,'apps/web/phase4-export-extensions-ui.js');
const start='<!-- SG_PHASE43_EXPORT_EXTENSIONS_UI_START -->';
const end='<!-- SG_PHASE43_EXPORT_EXTENSIONS_UI_END -->';
let html=fs.readFileSync(htmlPath,'utf8');
const js=fs.readFileSync(jsPath,'utf8');
const escStart=start.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
const escEnd=end.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
html=html.replace(new RegExp(escStart+'[\\s\\S]*?'+escEnd+'\\s*','g'),'');
const finalClose=/<\/body>\s*<\/html>\s*$/i;
if(!finalClose.test(html))throw new Error('Mungon mbyllja finale </body></html>.');
const block=start+'\n<script id="sg-phase43-export-extensions-script">\n'+js+'\n</script>\n'+end+'\n';
html=html.replace(finalClose,block+'</body>\n</html>');
fs.writeFileSync(htmlPath,html);
const check=fs.readFileSync(htmlPath,'utf8');
if((check.match(/SG_PHASE43_EXPORT_EXTENSIONS_UI_START/g)||[]).length!==1)throw new Error('Patch-i i zgjerimeve të eksportit nuk është idempotent.');
['Commercial Invoice janë të detyrueshme','openExportTimeline','openExportDocuments','15 raporte operative','exportCurrentExportReportExcel','SGPhase43ExportExtensions'].forEach(marker=>{if(!check.includes(marker))throw new Error('Mungon marker-i i eksportit: '+marker);});
console.log('Phase 4.3 export extensions UI patched.');
