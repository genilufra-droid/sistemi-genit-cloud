/* Build patch: inject Phase 6 Operations UI */
'use strict';
const fs=require('fs');
const path=require('path');
const root=path.resolve(__dirname,'..');
const htmlPath=path.join(root,'apps/web/index.html');
const jsPath=path.join(root,'apps/web/phase6-operations-ui.js');
const cssPath=path.join(root,'apps/web/phase6-operations-ui.css');
const start='<!-- SG_PHASE6_OPERATIONS_UI_START -->';
const end='<!-- SG_PHASE6_OPERATIONS_UI_END -->';
let html=fs.readFileSync(htmlPath,'utf8');
const js=fs.readFileSync(jsPath,'utf8');
const css=fs.readFileSync(cssPath,'utf8');
const escStart=start.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
const escEnd=end.replace(/[.*+?^${}()|[\]\\]/g,'\\$&');
html=html.replace(new RegExp(escStart+'[\\s\\S]*?'+escEnd+'\\s*','g'),'');
const finalClose=/<\/body>\s*<\/html>\s*$/i;
if(!finalClose.test(html))throw new Error('Mungon mbyllja finale </body></html>.');
const block=start+'\n<style id="sg-phase6-operations-style">\n'+css+'\n</style>\n<script id="sg-phase6-operations-script">\n'+js+'\n</script>\n'+end+'\n';
html=html.replace(finalClose,block+'</body>\n</html>');
fs.writeFileSync(htmlPath,html);
const check=fs.readFileSync(htmlPath,'utf8');
if((check.match(/SG_PHASE6_OPERATIONS_UI_START/g)||[]).length!==1)throw new Error('Patch-i Faza 6 nuk është idempotent.');
['SG_PHASE6_OPERATIONS_LOADER','DecompressionStream','sg6-kpis','phase6-operations-script'].forEach(function(marker){if(!check.includes(marker))throw new Error('Mungon '+marker+' në HTML.');});
console.log('Phase 6 Operations UI patched.');
